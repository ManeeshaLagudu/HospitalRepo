/* Declaring datatypes 
   Declaring variables */
int int_variable = 100 // Example of int datatype
println(int_variable);

long long_variable = 19846293018238109637 // Example of long datatype
println(long_variable);

float float_variable = 10.56f; // Example of float datatype
println(float_variable);

double double_variable = 10000.674781922334 // Example of double datatype
println(double_variable);

char character = 'A' // Example of char datatype
println(character);

String myString = "Groovy is a programming language" // Example of String datatype
println(myString);










