package Groovy
class Methods{
   int additionOfNumbers (int value1,int value2){
      return value1 + value2
   }
   int subtractionOfNumbers(value1, value2){
      return value1 - value2
   }
   int multiplicationOfNumbers(value1, value2){
       return value1 * value2
   }
   int divisionOfNumbers(value1, value2){
      return value1 / value2
   }
   int modulusOfNumbers(value1, value2){
      return value1 % value2
   }
   
}
